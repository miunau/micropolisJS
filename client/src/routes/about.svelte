    <h3 class="alignCenter chunk">Welcome to your city!</h3>
    <p>
      micropolisJS is a handmade port of <a href="http://code.google.com/p/micropolis/">Micropolis</a> to Javascript. (Micropolis is the open-source release of a commercial game once sold by Electronic Arts). The aim is to grow your city, balancing the various competing demands. Use the buttons in the toolbox to select the various zones and buildings, and then click on the desired location to build.
    </p>
      <ul>
      <li><strong>Residential zones:</strong> Your citizens need somewhere to live! Use this tool to lay out residential zones, and watch people move in. Watch out though: no-one wants to live anywhere polluted!</li>
      <li><strong>Commercial zones:</strong>Sometimes your citizens need to pop round to the shops for some milk. However, they're screwed if there are no shops! Commercial zones contain the shops and offices that will power the economy.</li>
      <li><strong>Industrial zones:</strong> There's only so much daytime television your citizens can watch. Provide industrial zones and put those shirkers to work!</li>
      <li><strong>Police Stations:</strong> There are always some bad apples. Build some police stations to bring down the hammer of justice and keep crime under control!</li>
      <li><strong>Fire Stations:</strong> Some people just want to play with matches. Sometimes a great big freaking monster comes along and stomps all over your town. Put out the flames by building fire stations.</li>
      <li><strong>Coal Power:</strong> Show your dominion over nature by filling that fresh air with honking big dusty clouds of smoke, and keep those lights on!</li>
      <li><strong>Nuclear Power:</strong> More expensive than coal power, but less polluting. These things are totally safe. Right?</li>
      <li><strong>Stadium:</strong> After a day struggling at those infernal industrial workhouses you erected, your citizens deserve to relax by watching the big game!</li>
      <li><strong>Airport:</strong> As your creation grows, your citizens will be seduced by the romantic dream of flying. Build an airport and let them meet the reality of weather delays and lousy service!</li>
      <li><strong>Seaport:</strong> Making stuff is hard work! As your town grows, your citizens will want huge container ships importing finished goods from halfway across the world.</li>
      <li><strong>Park:</strong> Sometimes you need to get away from it all. Parks provide a place to relax, get back to nature, and stare at the clouds of smoke from the power plant in the middle distance.</li>
      <li><strong>Road, Rail:</strong> Your citizens are going places. Unless you neglect to lay these.</li>
      <li><strong>Wire:</strong> Plug your citizens into the matrix. And to 240v AC.</li>
    </ul>
    <h3 class="alignCenter chunk">Tips</h3>
    <p>
      If you're starting to run out of money, use the budget tool to modify the tax rate, and funding for various city services. Don't cut things too much, or things will start to fall apart!</p>
    <p>
      The evaluation tool can provide insight into the opinion of the city's inhabitants, and the query tool offers finer-grained insight.
    </p>
    <h3 class="alignCenter chunk">Disasters</h3>
    <p>
      Some people just want to watch the world burn. If you're one of them, then the disasters button on the left hand side will give you the matches. Choose one of the options to unleash mayhem!
    </p>
    <ul>
      <li><strong>Fire:</strong> Turn your city into an advert for smoke detectors.</li>
      <li><strong>Flood:</strong> I hope your citizens can swim</li>
      <li><strong>Meltdown:</strong> <i>Nervously pats the nuclear reactor</i> <q>These things are safe&mdash;right?</q></li>
      <li><strong>Monster:</strong> Is it a bird? Is it a plane? Well, it's big, red and scary, so I'm not waiting to find out!</li>
      <li><strong>Plane crash:</strong> What goes up...</li>
      <li><strong>Tornado:</strong> Your citizens won't want to watch <cite>Twister</cite> on NetFlix for a while</li>
    </ul>
    <h3 class="alignCenter chunk">Settings</h3>
    <p>
      You will find assorted helpful controls by clicking the settings button on the left.
    </p>
    <ul>
      <li><strong>Auto-budget:</strong> The simulator takes care of finances for you&mdash;well, until you go broke at least&mdash;. If you prefer more control, you can switch this on or off here, and spend spend spend every January.</li>
      <li><strong>Auto-bulldoze:</strong> When building on areas that contain terrain other than plain dirt, these must be cleared first, at a cost. You can turn this off, and prepare your building sites manually.</li>
      <li><strong>Auto-disasters:</strong> What's more fun than manually unleashing mayhem? Why having it unleashed by surprise of course!</li>
      <li><strong>Speed:</strong> Time flies when you max out the game speed</li>
    </ul>
